// @lc code=start
/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
    ListNode* rotateRight(ListNode* head, int k) {
        if (!head || !head->next) return head;

        int len = 0;
        ListNode *cur = head;
        ListNode *tail = NULL;
        while(cur) {
            len++;
            cur = cur->next;
            tail = !cur ? tail : cur;
        }

        int step = k % len;   //移动的步数
        cur = head;
        for (int i = 0; i < len - step - 1; i++) {
            cur = cur->next;
        }
        tail->next = head;
        head = cur->next;
        cur->next = NULL;

        return head;
    }
};
// @lc code=end

